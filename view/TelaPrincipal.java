    private void jButton_BuscarVeterinario_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_BuscarVeterinario_ActionPerformed

        String CPF = JOptionPane.showInputDialog("Informe o CPF");
        String sqlId = "SELECT idveterinario FROM Veterinario WHERE cpf = ?";
        
        try {
            PreparedStatement ps = conn.getConexao().prepareStatement(sqlId);
            ps.setString(1, CPF);
            ResultSet rs = ps.executeQuery();
            
            if (rs.next()) {
                Buscar_V = rs.getInt("idveterinario");
                JOptionPane.showMessageDialog(null, "Busca Confirmada ");
            }else{
                JOptionPane.showMessageDialog(null, "Veterinario Não Cadastrado! ");
            }
            
            ps.close();
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "Erro SQL: " + ex.getMessage());
        }
        
    }//GEN-LAST:event_jButton_BuscarVeterinario_ActionPerformed

