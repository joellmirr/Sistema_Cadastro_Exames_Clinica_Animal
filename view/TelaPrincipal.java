    public AnimalM CriarAnimal(){
        AnimalM a = new AnimalM();
            a.setNomeAnimal(jTextField_nomeAnimal.getText());
            a.setEspecie(jTextField_especie.getText());
            a.setDataNas(jTextField_dataNascimento.getText());
            a.setSexo(jTextField_sexoAnimal.getText());
            a.setDescricaoAnimal(jTextArea_DescricaoAnimal.getText());
            a.setFazenda(Buscar_F);
            return a;
    }
       
    public ExameM MontarExame(){
        
        ExameM e = new ExameM();
        e.setTipoExame(jTextField_tipoExame.getText());
        e.setResulExame(jTextField_ResultadoExame.getText());
        e.setVacina(jTextField_Vascina.getText());
        e.setDataVacina(jTextField_dataVascina.getText());
        e.setPeso(jTextField_Peso.getText());
        e.setDataPeso(jTextField_dataPeso.getText());
        e.setVeterinario(Buscar_V);
        e.setAnimal(Buscar_A);
        return e;
    }
